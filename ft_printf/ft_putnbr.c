#include "ft_printf.h"

void	ft_putnbr(long nb, int *i)
{
	long	n;

	if (nb < 0)
	{
		ft_putchar('-');
		n = -nb;
		*i = *i + 1;
	}
	else
		n = nb;
	if (n > 9)
	{
		ft_putnbr(n / 10, i);
		n %= 10;
	}
	ft_putchar(n + '0');
	*i = *i + 1;
}
